import turtle
chris = turtle.Turtle()
chris.write("(0,0)")
chris.penup()
chris.goto(-300,0) # our turtle will go to this location
chris.write("(-300,0)")
chris.goto(300,0) # goto always needs 2 numbers, X then Y
chris.write("(300,0)")
chris.goto(0,-300)
chris.write("(0,-300)")
chris.goto(0,300)
chris.write("(0,300)")

